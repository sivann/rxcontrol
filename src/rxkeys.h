
/* Default keys used by rxcontrol (about 9)     */
/* Watch the case - Shift has the meaning "All" */

#define K_ENTER		'\n'

/*Main keys*/
#define K_VOLUP		'.'  /* volume down    */
#define K_VOLDOWN	','  /* volume up      */
#define K_QUIT		'R'  /* quit rxcontrol (and 'q') */

#define K_PTRACK 	'z'  /* previous track        */
#define K_PLAY		'x'  /* play                  */
#define K_PAUSE		'c'  /* pause                 */
#define K_STOP		'v'  /* stop                  */
#define K_NTRACK	'b'  /* next track            */
#define K_FF		']'  /* fast forward (seek)   */
#define K_FB		'['  /* fast backwards (seek) */
#define K_REPEAT	'r'  /* toggle repeat on/off  */

/*Change menu (operation mode) keys*/
#define K_FSEL		'f'  /* File select   */
#define K_PLEDIT	'p'  /* Edit Playlist */
#define K_PLBRSE	'l'  /* Browse pre-compiled playlists on disk */
#define K_MENUSEL	'u'  /* Select one of the above with a menu   */

/* LCD scroll keys */
#define K_SCRIGHT	']'  /* Scroll right   */
#define K_SCLEFT	'['  /* Scroll left    */
#define K_SCUP		'z'  /* Scroll up      */
#define K_SCTOP		'Z'  /* go to line 1   */
#define K_SCDOWN	'b'  /* Scroll down    */
#define K_SCBOTTOM	'B'  /* goto last line */


/*Menu Selection keys*/
#define K_MENUSEL_SEL	 'x' /* Select this one     */
#define K_MENUSEL_GOBACK 'u'
#define K_MENUSEL_UPGRD  'U' /* Upgrade from floppy */

/*Playlist editing menu keys*/
#define K_PLEDIT_REMOVE		'v' /* remove file            */
#define K_PLEDIT_CLEAR		'V' /* remove all files       */
#define K_PLEDIT_SAVE		'X' /* save playlist to PLDIR */
#define K_PLEDIT_GOBACK		'u' /* go to main screen      */
#define K_PLEDIT_RANDOMIZE	'r' /* randomize a playlist   */

/*Playlist browsing menu keys*/
#define K_PLBRSE_DEL	'v' /* delete file       */
#define K_PLBRSE_GOBACK	'u' /* go to main screen */
#define K_PLBRSE_SEL	'x' /* select file       */

/*File selection menu keys*/
#define K_FSEL_RANDSEL	'r' /* make a random playlist        */
#define K_FSEL_SELALL	'X' /* select everything in order    */
#define K_FSEL_CLEARPL	'V' /* clear playlist                */
#define K_FSEL_GOBACK	'u' /* go to main screen             */
#define K_FSEL_SEL	'x' /* Select this one               */
#define K_FSEL_RECSEL	'R' /* Recursive directory selection */

